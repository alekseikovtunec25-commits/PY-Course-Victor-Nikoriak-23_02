"""
app/config/settings.py — Конфігурація бота через змінні середовища.

РОЛЬ У АРХІТЕКТУРІ:
    Централізоване місце для ВСІХ налаштувань бота.
    Замість того, щоб розкидати os.getenv() по всіх файлах,
    всі параметри збираються тут у єдиному класі Config.

ЧОМУ ЗМІННІ СЕРЕДОВИЩА, А НЕ ЗАХАРДКОДЖЕНІ ЗНАЧЕННЯ:
    Hardcode:   BOT_TOKEN = "123456:ABC-..."   ← НЕБЕЗПЕЧНО!
    Env vars:   BOT_TOKEN = os.getenv("BOT_TOKEN")  ← ПРАВИЛЬНО

    Переваги env vars:
      1. Безпека — секрети не потрапляють у git (є у .gitignore)
      2. Гнучкість — різні токени для dev/staging/production
      3. 12-factor app — стандарт для cloud-deployments

ЯК ПРАЦЮЄ load_dotenv():
    Читає файл .env у поточній директорії:
        BOT_TOKEN=1234567890:AAF...
        GEMINI_API_KEY=AIzaSy...
    І завантажує ці значення у os.environ.
    Якщо змінна вже встановлена у реальному середовищі — НЕ перезаписує.
    Тому у Docker/production .env ігнорується на користь реальних env vars.

ЧОМУ SINGLETON config = Config():
    Ми створюємо ОДИН екземпляр Config наприкінці файлу.
    Всі інші модулі імпортують цей самий об'єкт:
        from app.config import config
    Це гарантує: конфігурація читається з .env ОДИН РАЗ.
"""
import os

# python-dotenv: зчитує .env файл і завантажує змінні у os.environ
# Якщо .env не існує — мовчки ігнорує (не падає з помилкою)
from dotenv import load_dotenv

# Завантажуємо .env при імпорті цього модуля
# Всі os.getenv() нижче вже "бачать" значення з .env
load_dotenv()


class Config:
    """
    Клас конфігурації бота.

    Кожен атрибут — одна налаштована опція.
    os.getenv("НАЗВА", "значення_за_замовчуванням"):
        - читає змінну середовища
        - якщо її немає — використовує default

    Типи:
        str  — рядкові параметри (токени, хости)
        int  — числові (порти, ліміти) — кастуємо через int(os.getenv(...))
    """

    # =========================================================
    # TELEGRAM
    # =========================================================
    # BOT_TOKEN — секретний токен від @BotFather.
    # Формат: "1234567890:AAF_AbCdEfGhIjKlMnOpQrStUvWxYz"
    # Частина перед ":" — числовий ID бота.
    # Частина після ":" — 35-символьний секрет.
    BOT_TOKEN: str = os.getenv("BOT_TOKEN", "")

    # =========================================================
    # GOOGLE GEMINI
    # =========================================================
    # GEMINI_API_KEY — ключ від Google AI Studio (aistudio.google.com).
    # Починається з "AIzaSy...".
    # Безкоштовна квота: 15 req/min, 1M tokens/day для Flash моделей.
    GEMINI_API_KEY: str = os.getenv("GEMINI_API_KEY", "")

    # GEMINI_MAX_TOKENS — максимальна довжина відповіді від AI.
    # 2048 токенів ≈ ~1500 слів ≈ ~6000 символів.
    # Більше токенів = довші відповіді, але повільніше і дорожче.
    GEMINI_MAX_TOKENS: int = int(os.getenv("GEMINI_MAX_TOKENS", "2048"))

    # GEMINI_MODEL — назва моделі за замовчуванням.
    # Використовується у вітальному повідомленні /start та /stats.
    # Реальний вибір моделі відбувається у ai_service.MODEL_POOL.
    GEMINI_MODEL: str = os.getenv("GEMINI_MODEL", "gemini-2.5-flash")

    # =========================================================
    # REDIS
    # =========================================================
    # Redis використовується для трьох цілей:
    #   1. Зберігання историї розмов (history:{user_id})
    #   2. Rate limiting (rate:{user_id})
    #   3. Circuit Breaker стану (ai_bot:cb:*)

    # REDIS_HOST — адреса сервера Redis.
    # При локальному запуску: localhost
    # У Docker Compose: назва сервісу, наприклад "redis"
    REDIS_HOST: str = os.getenv("REDIS_HOST", "localhost")

    # REDIS_PORT — порт Redis (стандартний 6379)
    REDIS_PORT: int = int(os.getenv("REDIS_PORT", "6379"))

    # REDIS_DB — номер бази даних всередині Redis (0-15).
    # Redis підтримує до 16 ізольованих БД в одному сервері.
    # 0 — за замовчуванням для більшості проєктів.
    REDIS_DB: int = int(os.getenv("REDIS_DB", "0"))

    # =========================================================
    # RATE LIMITING
    # =========================================================
    # Захищає від спаму та зловживань.
    # Алгоритм: sliding window counter (Redis INCR + EXPIRE).

    # RATE_LIMIT_REQUESTS — скільки повідомлень дозволено за вікно часу.
    # Default: 5 запитів
    RATE_LIMIT_REQUESTS: int = int(os.getenv("RATE_LIMIT_REQUESTS", "5"))

    # RATE_LIMIT_WINDOW — розмір часового вікна у секундах.
    # Default: 60 секунд
    # Разом: не більше 5 запитів за 60 секунд = 1 запит/12 сек в середньому.
    RATE_LIMIT_WINDOW: int = int(os.getenv("RATE_LIMIT_WINDOW", "60"))

    # =========================================================
    # CHAT HISTORY
    # =========================================================
    # HISTORY_MAX_MESSAGES — скільки останніх повідомлень зберігати.
    # Більший контекст = краща якість відповідей AI.
    # Але: більший контекст = більше токенів = вищі витрати квоти.
    # 20 повідомлень (10 пар user/assistant) — розумний баланс.
    HISTORY_MAX_MESSAGES: int = int(os.getenv("HISTORY_MAX_MESSAGES", "20"))

    # =========================================================
    # ЛОГУВАННЯ
    # =========================================================
    # LOG_LEVEL — рівень деталізації логів.
    # DEBUG   — найдетальніший (для розробки)
    # INFO    — нормальний потік (production default)
    # WARNING — тільки потенційні проблеми
    # ERROR   — тільки помилки
    # CRITICAL — критичні збої
    LOG_LEVEL: str = os.getenv("LOG_LEVEL", "INFO")

    # =========================================================
    # SYSTEM PROMPT
    # =========================================================
    # Системний промт — це "особистість" AI-асистента.
    # Він надсилається до Gemini у кожному запиті як system_instruction.
    # Визначає: мову відповідей, стиль, обмеження, роль.
    #
    # Чому тут, а не у .env?
    #   Бо це частина бізнес-логіки, а не секрет.
    #   Секрети (токени, ключі) — у .env.
    #   Конфігурація поведінки — у коді.
    SYSTEM_PROMPT: str = (
        "Ти — корисний AI-асистент, вбудований у Telegram-бот. "
        "Відповідай українською мовою, якщо користувач не просить інше. "
        "Будь лаконічним та конкретним. "
        "Не маєш доступу до інтернету та поточних новин. "
        "Якщо не знаєш відповіді — чесно скажи про це."
    )

    def validate(self) -> None:
        """
        Перевіряє, що всі обов'язкові змінні встановлені.

        Викликається на старті (main.py) до будь-якої ініціалізації.
        Якщо є помилки — збирає ВСІ проблеми разом і викидає ValueError.

        Чому збираємо всі помилки, а не падаємо на першій?
            Зручніше для розробника: одразу бачить всі відсутні змінні,
            а не виправляє одну — запускає — бачить наступну — і так по колу.
        """
        errors = []

        # Перевіряємо Telegram token
        if not self.BOT_TOKEN:
            errors.append("BOT_TOKEN не встановлено")

        # Перевіряємо Gemini API key
        if not self.GEMINI_API_KEY:
            errors.append("GEMINI_API_KEY не встановлено")

        # Якщо є хоч одна помилка — піднімаємо виняток зі списком проблем
        if errors:
            raise ValueError(
                "Помилки конфігурації:\n" + "\n".join(f"  - {e}" for e in errors)
            )


# =========================================================
# SINGLETON ЕКЗЕМПЛЯР
# =========================================================
# Створюємо ОДИН об'єкт конфігурації на рівні модуля.
# Всі модулі, що роблять "from app.config import config",
# отримують посилання на ЦЕЙ САМИЙ об'єкт.
#
# Python кешує імпорти — модуль завантажується лише раз,
# тому Config() викликається лише один раз за весь lifetime процесу.
config = Config()
