"""
app/utils/text.py — Утиліти для роботи з текстом.

РОЛЬ У АРХІТЕКТУРІ:
    Допоміжні функції для безпечної роботи з текстом у Telegram-повідомленнях.

    Telegram має два обмеження, про які потрібно пам'ятати:
        1. Максимальна довжина повідомлення: 4096 символів
        2. При parse_mode=HTML спецсимволи < > & треба екранувати

    Ці функції вирішують обидві проблеми.

ЧОМУ truncate ПОТРІБЕН:
    Якщо надіслати повідомлення довше 4096 символів — Telegram поверне помилку 400.
    truncate() безпечно обрізає текст, залишаючи "..." для позначення скорочення.

ЧОМУ escape_html ПОТРІБЕН:
    При parse_mode=HTML Telegram інтерпретує теги <b>, <i> тощо.
    Але якщо у КОНТЕНТІ є символи < або > — Telegram думає це HTML теги.
    Приклад:
        Без escape: "3 < 5 і 10 > 7"
            Telegram: "3 " (все після < вирізається як тег)
        З escape:   "3 &lt; 5 і 10 &gt; 7"
            Telegram: "3 < 5 і 10 > 7" ✓

    HTML entities:
        &  → &amp;   (завжди першим! інакше escape підвоїться)
        <  → &lt;
        >  → &gt;
"""


def truncate(text: str, max_length: int = 4096) -> str:
    """
    Обрізає текст до вказаного ліміту символів.

    Параметри:
        text       — вхідний текст
        max_length — максимальна кількість символів (default: 4096 = ліміт Telegram)

    Повертає:
        Оригінальний текст, якщо він коротший за ліміт.
        Обрізаний текст + "..." якщо довший.

    Приклад:
        truncate("Привіт світ!", max_length=10)
        → "Привіт..."    (7 символів "Привіт" + 3 крапки = 10)

    Деталь: max_length - 3
        Залишаємо місце для "..." (3 символи).
        text[:max_length - 3] + "..." == text[:max_length] за довжиною.
    """
    # Якщо текст вміщується — повертаємо без змін
    if len(text) <= max_length:
        return text

    # Обрізаємо і додаємо "..." щоб показати що текст не повний
    return text[: max_length - 3] + "..."


def escape_html(text: str) -> str:
    """
    Екранує HTML-спецсимволи для безпечного відображення у parse_mode=HTML.

    Замінює символи, що мають спеціальне значення у HTML:
        & → &amp;   (амперсанд — початок HTML entity)
        < → &lt;    (менше — початок HTML тегу)
        > → &gt;    (більше — кінець HTML тегу)

    ВАЖЛИВО: замінюємо & ПЕРШИМ.
        Якщо спочатку замінити < на &lt;, а потім & на &amp;:
            "&lt;" стане "&amp;lt;" — подвійне екранування!
        Правильний порядок: & → < → >

    Приклад:
        escape_html('<b>Tom & Jerry</b>')
        → '&lt;b&gt;Tom &amp; Jerry&lt;/b&gt;'

        Після escape Telegram відобразить: <b>Tom & Jerry</b>
        (буквально, без HTML-рендерингу)

    Коли використовувати:
        - При показі контенту від користувача у HTML-повідомленнях
        - При відображенні коду або технічних рядків з < > &
        - При відображенні даних з бази даних (SQL, шляхи тощо)
    """
    # Порядок КРИТИЧНИЙ: & замінюємо першим
    return (
        text
        .replace("&", "&amp;")   # 1. амперсанд → HTML entity
        .replace("<", "&lt;")    # 2. менше → HTML entity
        .replace(">", "&gt;")    # 3. більше → HTML entity
    )
