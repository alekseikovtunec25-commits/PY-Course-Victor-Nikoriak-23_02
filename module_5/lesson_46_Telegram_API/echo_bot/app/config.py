"""
app/config.py — Конфігурація Echo-бота через змінні середовища.

РОЛЬ У АРХІТЕКТУРІ:
    Централізоване місце для ВСІХ налаштувань бота.
    Всі os.getenv() зібрані тут — інші модулі просто імпортують `config`.

ЧОМУ ЗМІННІ СЕРЕДОВИЩА, А НЕ ЗАХАРДКОДЖЕНІ ЗНАЧЕННЯ:
    ❌ Погано (hardcode):
        BOT_TOKEN = "1234567890:AAF_AbCdEfGh..."
        Якщо цей рядок потрапить у git — токен скомпрометований назавжди!

    ✅ Правильно (env vars):
        BOT_TOKEN = os.getenv("BOT_TOKEN")
        Токен зберігається у .env (є у .gitignore), у git не потрапляє.

    Переваги env vars:
      1. Безпека  — секрети не в коді
      2. Гнучкість — dev/staging/production мають різні токени
      3. 12-factor — стандарт хмарних застосунків

ЯК ПРАЦЮЄ load_dotenv():
    Читає файл .env з поточної директорії:
        BOT_TOKEN=1234567890:AAF...
        LOG_LEVEL=DEBUG

    Завантажує значення у os.environ.
    Якщо змінна вже є у реальному середовищі (наприклад, Docker env) —
    НЕ перезаписує (реальне env має пріоритет над .env файлом).

SINGLETON ПАТЕРН:
    Наприкінці файлу: config = Config()
    Всі модулі: from app.config import config
    → Отримують один і той самий об'єкт (Python кешує імпорти).
    Config() викликається лише один раз за lifetime процесу.
"""
import os

# python-dotenv: зчитує .env файл і завантажує змінні у os.environ
# Якщо .env не існує — мовчки ігнорує (не падає)
from dotenv import load_dotenv

# Завантажуємо .env при імпорті модуля
# Всі os.getenv() нижче вже "бачать" значення з .env
load_dotenv()


class Config:
    """
    Клас конфігурації Echo-бота.

    Кожен атрибут — одна налаштована опція.

    os.getenv("НАЗВА", "default"):
        Читає змінну середовища.
        Якщо змінна не встановлена — повертає default значення.

    Типи:
        str — рядкові параметри (токен, рівень логу)
    """

    # =========================================================
    # TELEGRAM BOT TOKEN
    # =========================================================
    # Секретний токен від @BotFather.
    # Формат: "1234567890:AAF_AbCdEfGhIjKlMnOpQrStUvWxYz"
    #          ^числовий ID   ^35-символьний секрет
    #
    # НІКОЛИ не публікувати цей токен у git/GitHub/публічних місцях!
    # Якщо токен скомпрометовано — відкликати через @BotFather → /revoke
    BOT_TOKEN: str = os.getenv("BOT_TOKEN", "")

    # =========================================================
    # РІВЕНЬ ЛОГУВАННЯ
    # =========================================================
    # Визначає деталізацію логів у терміналі.
    #
    # DEBUG   → ВСЕ: кожен крок, повні JSON дампи
    # INFO    → нормальні події (default для production)
    # WARNING → підозрілі ситуації
    # ERROR   → помилки
    # CRITICAL → критичні збої, бот падає
    #
    # Default: DEBUG — зручно під час розробки.
    # У production краще: INFO (менше шуму у логах).
    LOG_LEVEL: str = os.getenv("LOG_LEVEL", "DEBUG")

    def validate(self) -> None:
        """
        Перевіряє що обов'язкові змінні встановлені.

        Викликається на старті (main.py) до будь-якої ініціалізації.

        Якщо BOT_TOKEN відсутній — падаємо з детальним повідомленням:
            "BOT_TOKEN не знайдено!
             1. Скопіюйте .env.example → .env
             2. Вставте токен від @BotFather у BOT_TOKEN"

        Чому краще упасти тут з ValueError, ніж пізніше?
            Без токена Bot.__init__() впаде з cryptic помилкою.
            Явна перевірка дає чіткий контекст: що пішло не так і як виправити.
        """
        if not self.BOT_TOKEN:
            raise ValueError(
                "BOT_TOKEN не знайдено!\n"
                "1. Скопіюйте .env.example → .env\n"
                "2. Вставте токен від @BotFather у BOT_TOKEN"
            )


# =========================================================
# SINGLETON ЕКЗЕМПЛЯР
# =========================================================
# Один об'єкт конфігурації на весь застосунок.
# Імпортується у всіх модулях: from app.config import config
# Python гарантує: цей рядок виконується лише один раз.
config = Config()
