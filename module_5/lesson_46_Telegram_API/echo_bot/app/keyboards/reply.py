"""
app/keyboards/reply.py — Reply-клавіатури для Echo-бота.

РОЛЬ У АРХІТЕКТУРІ:
    Цей файл — UI Layer бота.
    Handlers не повинні вручну конструювати кнопки — вони викликають get_main_keyboard().

    Аналогія з frontend:
        keyboard.py ↔ React Component
        handlers/   ↔ Page/Controller (використовує компоненти)

    Якщо змінити кнопки — правиш лише keyboard.py.
    Handlers не знають деталей UI — вони просто отримують готовий reply_markup.

ДВА ТИПИ КЛАВІАТУР У TELEGRAM:

    1. ReplyKeyboardMarkup (цей файл):
       ┌────────────────────────────────────┐
       │ [ℹ️ Про бота]  [❓ Допомога]       │ ← великі кнопки внизу
       └────────────────────────────────────┘
       - З'являється ЗАМІСТЬ стандартної клавіатури
       - При натисканні: надсилає ТЕКСТ як повідомлення
         (message.text = "ℹ️ Про бота")
       - Залишається видимою між повідомленнями
       - Використовується для: головне меню, FAQ, навігація

    2. InlineKeyboardMarkup (для ai_bot):
       ┌──────────────────────────────────────────┐
       │ Привіт! Чим можу допомогти?              │
       │ [🔄 Скинути контекст] [📊 Статистика]   │ ← кнопки всередині повідомлення
       └──────────────────────────────────────────┘
       - Прикріплені до конкретного повідомлення
       - При натисканні: генерується CallbackQuery (не текстове повідомлення)
       - Зникають разом з повідомленням
       - Використовується для: підтвердження, вибір опцій, пагінація

ЯК HANDLERS ОТРИМУЮТЬ НАТИСКАННЯ КНОПОК:
    Кнопка [ℹ️ Про бота] → message.text = "ℹ️ Про бота"
    Handler: @router.message(F.text == "ℹ️ Про бота")

    Кнопки ReplyKeyboard = просте надсилання тексту.
    НЕ ПЛУТАТИ з InlineKeyboard, де потрібен @router.callback_query.
"""
from aiogram.types import (
    ReplyKeyboardMarkup,  # Контейнер для reply-кнопок
    KeyboardButton,       # Одна кнопка клавіатури
    ReplyKeyboardRemove,  # Команда "прибрати клавіатуру"
)


def get_main_keyboard() -> ReplyKeyboardMarkup:
    """
    Повертає головну Reply-клавіатуру бота.

    Структура клавіатури (один ряд, дві кнопки горизонтально):
        [ ℹ️ Про бота ]  [ ❓ Допомога ]

    ReplyKeyboardMarkup:
        keyboard — список рядів кнопок:
            [ [кнопка1, кнопка2] ]  ← один ряд
            [ [кнопка1], [кнопка2] ] ← два ряди
        resize_keyboard — Telegram адаптує висоту клавіатури
        input_field_placeholder — placeholder у полі введення тексту

    KeyboardButton(text="..."):
        text — текст кнопки (відображається на кнопці І надсилається як повідомлення).
        Інші параметри KeyboardButton:
            request_contact  — запросити номер телефону
            request_location — запросити геолокацію
            request_poll     — запросити опитування

    Чому emoji у тексті кнопок "ℹ️" і "❓"?
        Emoji роблять кнопки помітнішими і зрозумілішими без підписів.
        Telegram підтримує emoji у тексті кнопок нативно.

    Повертає:
        ReplyKeyboardMarkup — готовий об'єкт для reply_markup параметра answer().
        Використання: await message.answer("Привіт!", reply_markup=get_main_keyboard())
    """

    # ── Структура кнопок ──────────────────────────────────────────────────
    # Список рядів (список списків).
    # Кожен внутрішній список = один рядок кнопок.
    # Два елементи в одному ряду → два кнопки поруч горизонтально.
    buttons = [
        [
            # Кнопка 1: "Про бота"
            # При натисканні → message.text = "ℹ️ Про бота"
            # Handler у start.py: @router.message(F.text == "ℹ️ Про бота")
            KeyboardButton(text="ℹ️ Про бота"),

            # Кнопка 2: "Допомога"
            # При натисканні → message.text = "❓ Допомога"
            # Handler у start.py: @router.message(F.text == "❓ Допомога")
            KeyboardButton(text="❓ Допомога"),
        ],
        # Тут можна додати другий ряд кнопок:
        # [KeyboardButton(text="📊 Статистика"), KeyboardButton(text="⚙️ Налаштування")]
    ]

    return ReplyKeyboardMarkup(
        keyboard=buttons,

        # resize_keyboard=True:
        #   Telegram автоматично підбирає висоту клавіатури.
        #   False → клавіатура займає весь екран (незручно для 2 кнопок).
        #   True → мінімальна висота, не перекриває чат.
        resize_keyboard=True,

        # input_field_placeholder:
        #   Текст-підказка у порожньому полі введення повідомлення.
        #   Аналог HTML: <input placeholder="Напиши щось...">
        #   Підказує користувачу що він може писати текст, не тільки натискати кнопки.
        input_field_placeholder="Напиши щось...",
    )


def remove_keyboard() -> ReplyKeyboardRemove:
    """
    Повертає команду видалення Reply-клавіатури.

    ReplyKeyboardRemove — спеціальний тип reply_markup.
    Коли надсилається боту: Telegram прибирає кастомну клавіатуру
    і повертає стандартну системну.

    Використання:
        await message.answer(
            "Клавіатуру прибрано.",
            reply_markup=remove_keyboard()
        )

    Коли використовувати:
        - Після завершення певного флоу (реєстрація, оплата)
        - Коли потрібно, щоб user вводив вільний текст без підказок
        - При переключенні у режим, де кнопки неактуальні

    Важливо: ReplyKeyboardRemove впливає ЛИШЕ на поточний чат.
    """
    return ReplyKeyboardRemove()
