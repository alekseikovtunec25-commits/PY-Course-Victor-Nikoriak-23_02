def test_reverse_string():
    """
    ЩО ПЕРЕВІРЯЄМО: звичайний рядок перевертається правильно.

    НАВІЩО: базовий happy path тест — перевіряємо основну функцію.
    "hello" → "olleh" — найтиповіший вхід.

    ЯК ЧИТАТИ ТЕСТ:
      Arrange: рядок 'hello' (вхідні дані)
      Act:     reverse_string('hello') (виконання)
      Assert:  результат == 'olleh' (очікуваний вихід)
    """
    result = reverse_string("hello")
    assert result == "olleh"


def test_reverse_empty_string():
    """
    ЩО ПЕРЕВІРЯЄМО: порожній рядок після реверсу залишається порожнім.

    НАВІЩО: edge case (крайній випадок). Порожній рядок — це валідний
    вхід, і функція НЕ повинна кидати виняток або повертати None.
    ""[::-1] == "" — це математично коректно.

    РЕАЛЬНИЙ СЦЕНАРІЙ: уяви, що reverse_string() використовується для
    обробки user input. Якщо користувач нічого не ввів — функція
    отримає "" і має коректно повернути "".
    """
    result = reverse_string("")
    assert result == ""


def test_reverse_single_char():
    """
    ЩО ПЕРЕВІРЯЄМО: рядок з одного символу після реверсу незмінний.

    НАВІЩО: ще один edge case. Один символ — мінімальний не-порожній
    вхід. "a"[::-1] == "a". Перевіряємо що функція не робить зайвого.

    ПРИНЦИП: для рядків завжди тестуй: порожній, один символ, звичайний.
    Це три "рівні" що покривають більшість помилок.
    """
    result = reverse_string("a")
    assert result == "a"


# ─────────────────────────────────────────────────────────────────────────────
# ТЕСТИ ДЛЯ ФУНКЦІЇ clamp()
# ─────────────────────────────────────────────────────────────────────────────
#
# clamp() — функція з ТРЬОМА різними гілками виконання:
#   1. value в межах → повертає value (жодного обрізання)
#   2. value < min   → повертає min   (обрізання знизу)
#   3. value > max   → повертає max   (обрізання зверху)
#
# Для кожної гілки — окремий тест. Це правило "one assertion per test":
# один тест — одна поведінка.
# ─────────────────────────────────────────────────────────────────────────────

def test_clamp_within_range():
    """
    ЩО ПЕРЕВІРЯЄМО: значення в межах діапазону не змінюється.

    СЦЕНАРІЙ: value=5, діапазон [0, 10] → 5 в межах → повертаємо 5.

    НАВІЩО: перевіряємо що функція НЕ змінює коректне значення.
    Типова помилка в реалізації: завжди обрізати до min або max.

    РЕАЛЬНИЙ ПРИКЛАД: у Django пріоритет нотатки 1-4. Якщо приходить
    priority=2 — clamp(2, 1, 4) має повернути 2 без змін.
    """
    result = clamp(5, 0, 10)
    assert result == 5   # 5 ∈ [0, 10] → без змін


def test_clamp_below_min():
    """
    ЩО ПЕРЕВІРЯЄМО: значення нижче мінімуму замінюється на мінімум.

    СЦЕНАРІЙ: value=-5, діапазон [0, 10] → -5 < 0 → повертаємо 0.

    НАВІЩО: перевіряємо "нижнє обрізання". Це одна з трьох гілок
    функції. Без цього тесту помилка у нижньому обрізанні лишиться
    непоміченою, навіть якщо верхнє обрізання працює правильно.

    РЕАЛЬНИЙ ПРИКЛАД: кількість товарів не може бути від'ємною.
    clamp(user_input, 0, 999) захищає від від'ємних значень.
    """
    result = clamp(-5, 0, 10)
    assert result == 0   # -5 < 0 (min) → обрізаємо до 0


def test_clamp_above_max():
    """
    ЩО ПЕРЕВІРЯЄМО: значення вище максимуму замінюється на максимум.

    СЦЕНАРІЙ: value=15, діапазон [0, 10] → 15 > 10 → повертаємо 10.

    НАВІЩО: перевіряємо "верхнє обрізання". Третя гілка функції.

    РЕАЛЬНИЙ ПРИКЛАД: прогрес-бар не може перевищити 100%.
    clamp(progress, 0, 100) — якщо через баг progress став 150,
    відображається коректно.

    ПІДСУМОК: три тести для clamp() = три гілки виконання.
    Покриваємо ВСІ можливі сценарії без зайвих тестів.
    """
    result = clamp(15, 0, 10)
    assert result == 10   # 15 > 10 (max) → обрізаємо до 10


