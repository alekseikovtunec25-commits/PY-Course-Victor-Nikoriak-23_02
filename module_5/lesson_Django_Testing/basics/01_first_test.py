# ─────────────────────────────────────────────────────────────────────────────
# Функції, які ми тестуємо
# (Вони могли бути в іншому модулі — тут для простоти в одному файлі)
# ─────────────────────────────────────────────────────────────────────────────

def add(a, b):
    """Додає два числа. Проста функція для демонстрації тестування."""
    return a + b


def is_even(n):
    """Перевіряє, чи число парне. Повертає True/False."""
    return n % 2 == 0


def reverse_string(s):
    """Перевертає рядок: 'hello' → 'olleh'."""
    return s[::-1]


def clamp(value, min_val, max_val):
    """
    Обрізає value до діапазону [min_val, max_val].
    Якщо value < min_val → повертає min_val.
    Якщо value > max_val → повертає max_val.
    Якщо value в межах → повертає value без змін.
    """
    return max(min_val, min(value, max_val))


# ─────────────────────────────────────────────────────────────────────────────
# ТЕСТИ ДЛЯ ФУНКЦІЇ add()
# ─────────────────────────────────────────────────────────────────────────────
#
# Чому три окремі тести для add(), а не один?
#
# Відповідь: кожен тест — окремий сценарій.
# Якщо один провалиться — відразу зрозуміло ЩО зламалось.
# "test_add_negative_numbers FAILED" — одразу зрозуміло де проблема.
# ─────────────────────────────────────────────────────────────────────────────

def test_add_positive_numbers():
    """
    ЩО ПЕРЕВІРЯЄМО: базове додавання двох додатних чисел.

    НАВІЩО: це найпростіший і найочевидніший сценарій. Якщо він падає —
    функція add() зламана повністю.

    ЧОМУ ЦЕ ВАЖЛИВО: тест є "smoke test" — перша перевірка що взагалі
    щось працює. Якщо навіть 2 + 3 != 5, далі нема сенсу перевіряти.
    """
    # Arrange
    a, b = 2, 3

    # Act
    result = add(a, b)

    # Assert — очікуємо рівно 5
    assert result == 5


def test_add_negative_numbers():
    """
    ЩО ПЕРЕВІРЯЄМО: додавання двох від'ємних чисел.

    НАВІЩО: від'ємні числа — окремий сценарій. Наприклад, реалізація
    add(a, b): return abs(a) + abs(b)  — дала б правильний результат
    для позитивних, але провалилась би тут.

    ЧОМУ ЦЕ ВАЖЛИВО: у реальних проєктах функції часто приймають
    від'ємні значення (температура, баланс, координати). Без цього тесту
    такі баги непомітні.
    """
    result = add(-1, -2)
    assert result == -3


def test_add_zero():
    """
    ЩО ПЕРЕВІРЯЄМО: додавання числа з нулем (нейтральний елемент).

    НАВІЩО: нуль — "граничне значення" (boundary value). Помилки часто
    виникають саме на межах: add(5, 0) має повернути 5, а не 0 чи інше.

    ПРИНЦИП тестування меж: завжди перевіряй 0, порожній рядок, None,
    пустий список — це де найчастіше ховаються баги.
    """
    result = add(5, 0)
    assert result == 5


# ─────────────────────────────────────────────────────────────────────────────
# ТЕСТИ ДЛЯ ФУНКЦІЇ is_even()
# ─────────────────────────────────────────────────────────────────────────────

def test_is_even_true():
    """
    ЩО ПЕРЕВІРЯЄМО: парне число повертає True.

    НАВІЩО: перевіряємо "позитивний сценарій" — коли функція має
    повернути True. Це основний happy path.

    ДЕТАЛЬ: ми пишемо  is True  (а не просто  is_even(4)),
    бо хочемо переконатись що повертається саме булеве True,
    а не просто truthy значення (наприклад, число 4 теж truthy).
    """
    result = is_even(4)
    assert result is True   # саме True, не просто truthy


def test_is_even_false():
    """
    ЩО ПЕРЕВІРЯЄМО: непарне число повертає False.

    НАВІЩО: "негативний сценарій". Функція has повертати False для
    непарних. Якщо хтось написав return n % 2 — він поверне 1 замість
    True для непарних, і цей тест це виявить.
    """
    result = is_even(7)
    assert result is False


def test_is_even_zero():
    """
    ЩО ПЕРЕВІРЯЄМО: нуль визначається як парне число.

    НАВІЩО: нуль — типова пастка. По математиці 0 ÷ 2 = 0 (без
    залишку), отже 0 ПАРНЕ. Але деякі розробники забувають про це
    або помилково думають що 0 — це "непарне" або "не число".

    0 % 2 == 0 → is_even(0) має повернути True.

    ТЕСТУВАННЯ ГРАНИЧНИХ ЗНАЧЕНЬ (boundary testing) — одна з ключових
    технік. Для is_even() межа — це 0, від'ємні числа, великі числа.
    """
    result = is_even(0)
    assert result is True   # 0 — парне по математиці!


